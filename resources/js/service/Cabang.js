import Http from '../http'

export default {
    getallCabang(){
        return new Promise((resolve, reject) => {
            
            const successCallback = (res) => {
                const data = res.data
                resolve(data)
            }
            const errorCallback = (err) => {
                reject(err)
            }
            Http.get('/api/cabangs', successCallback, errorCallback)
        })
    },
    addCabang(payload) {
        return new Promise((resolve, reject) => {
            const successCallback = (res) => {
                const data = res.data
                resolve(data)
            }
            const errorCallback = (err) => {
                reject(err)
            }
            Http.post('/api/cabangs/store', payload, successCallback, errorCallback)
        })
    },
    updateCabang(payload,id){
        return new Promise((resolve, reject) => {
            const successCallback = (res) => {
                const data = res.data
                resolve(data)
            }

            const errorCallback = (err) => {
                reject(err)
            }

            Http.patch('/api/cabangs/update/'+id, payload, successCallback, errorCallback)
        })
    },
    deleteCabang(id){
        return new Promise((resolve, reject) => {
            const successCallback = (res) => {
                const data = res.data
                resolve(data)
            }
            const errorCallback = (err) => {
                reject(err)
            }
            Http.delete('/api/cabangs/delete/'+id, successCallback, errorCallback)
        })
    }
}
