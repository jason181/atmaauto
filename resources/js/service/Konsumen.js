import Http from '../http'

export default {
    getallkonsumen(){
        return new Promise((resolve, reject) => {
            
            const successCallback = (res) => {
                const data = res.data
                resolve(data)
            }
            const errorCallback = (err) => {
                reject(err)
            }
            Http.get('/api/konsumens', successCallback, errorCallback)
        })
    },
    addkonsumen(payload) {
        return new Promise((resolve, reject) => {
            const successCallback = (res) => {
                const data = res.data
                resolve(data)
            }
            const errorCallback = (err) => {
                reject(err)
            }
            Http.post('/api/konsumens/store', payload, successCallback, errorCallback)
        })
    },
    updatekonsumen(payload,id){
        return new Promise((resolve, reject) => {
            const successCallback = (res) => {
                const data = res.data
                resolve(data)
            }

            const errorCallback = (err) => {
                reject(err)
            }

            Http.patch('/api/konsumens/update/'+id, payload, successCallback, errorCallback)
        })
    },
    deletekonsumen(id){
        return new Promise((resolve, reject) => {
            const successCallback = (res) => {
                const data = res.data
                resolve(data)
            }
            const errorCallback = (err) => {
                reject(err)
            }
            Http.delete('/api/konsumens/delete/'+id, successCallback, errorCallback)
        })
    }
}
